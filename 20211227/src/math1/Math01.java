package math1;

public class Math01 {

	public static void main(String[] args) {
		// Math는 자바에서 기본으로 제공하는 기능
		// import 없이 사용가능 합니다
		
		int a = - 1024;
		System.out.println(Math.abs(a));
		
		double b = 3.14;
		System.out.println(Math.ceil(b));
		//ceil 올림
		
		double c = 19.9;
		System.out.println(Math.floor(c));
		// floor 내림
		
		

	}

}
